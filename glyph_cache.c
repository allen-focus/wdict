// TODO: For small font size, ClearType provides a better appearance compared to GrayScale. In the future,
// we might want to implement ClearType support. The [dwrite-hlsl](https://github.com/lhecker/dwrite-hlsl)
// library could be useful for blending when this change is made.
//
// NOTE: the bbox (bound) of ' ' glyph is (0, 0, 0, 0).
// NOTE: `IDWriteGlyphRunAnalysis_CreateAlphaTexture` does support rasterize multi glyphs, but I have no idea how to get
// each glyph metrics of the bitmap generated by it. If just rasterize one glyph one time, then xoff, yoff just equal
// bound_left/top.
static u8* glyph_get(IDWriteFactory3* dwrite_factory, Font* font, const u16 codepoint, Glyph* glyph)
{
    glyph->font = font;

    // Get pixel size & scale, see: https://learn.microsoft.com/en-us/windows/win32/learnwin32/dpi-and-device-independent-pixels
    DWRITE_FONT_METRICS font_metrics = { 0 };
    IDWriteFontFace_GetMetrics(font->face, &font_metrics);
    f32 pixel_size = font->size * (font->dpi / USER_DEFAULT_SCREEN_DPI);
    f32 scale = pixel_size / (f32)font_metrics.capHeight;

    glyph->font->ascent = (u16)((f32)font_metrics.ascent * scale);
    glyph->font->descent = (u16)((f32)font_metrics.descent * scale);
    glyph->font->line_gap = (i16)((f32)font_metrics.lineGap * scale);
    glyph->font->capital_letter_height = (u16)((f32)font_metrics.capHeight * scale);

    // Get glyph indices
    glyph->codepoint = codepoint;
    u32 codepoints[1] = { glyph->codepoint };
    u16 glyph_indices[1] = { 0 };
    IDWriteFontFace_GetGlyphIndices(font->face, codepoints, 1, glyph_indices);

    // Get glyph advances & glyph offsets
    DWRITE_GLYPH_METRICS design_metrics[1] = { 0 };
    IDWriteFontFace3_GetDesignGlyphMetrics(font->face3, glyph_indices, 1, design_metrics, FALSE);
    f32 glyph_advances[1] = { design_metrics[0].advanceWidth * scale };
    DWRITE_GLYPH_OFFSET glyph_offsets[1] = { 0 };
    glyph->xadvance = (u16)glyph_advances[0];

    // Glyph run analysis
    DWRITE_GLYPH_RUN run = {
        .fontFace = font->face,
        .fontEmSize = (f32)font_metrics.designUnitsPerEm * scale,
        .glyphCount = 1,
        .glyphIndices = glyph_indices,
        .glyphAdvances = glyph_advances,
        .glyphOffsets = glyph_offsets,
    };

    DWRITE_GRID_FIT_MODE grid_fit_mode = DWRITE_GRID_FIT_MODE_DEFAULT;
    DWRITE_RENDERING_MODE1 rendering_mode = DWRITE_RENDERING_MODE1_NATURAL_SYMMETRIC;

    IDWriteGlyphRunAnalysis* analysis = NULL;
    IDWriteFactory3_CreateGlyphRunAnalysis2(
        /* this             */ dwrite_factory,
        /* glyphRun         */ &run,
        /* transform        */ NULL,
        /* renderingMode    */ rendering_mode,
        /* measuringMode    */ DWRITE_MEASURING_MODE_NATURAL,
        /* gridFitMode      */ grid_fit_mode,
        /* antialiasMode    */ DWRITE_TEXT_ANTIALIAS_MODE_GRAYSCALE,
        /* baselineOriginX  */ 0.0f,
        /* baselineOriginY  */ 0.0f,
        /* glyphRunAnalysis */ &analysis);

    // Create glyph bitmap
    u8* glyph_bitmap;
    {
        // DWRITE_TEXTURE_ALIASED_1x1 is a misnomer, It actually outputs grayscale if we set up the analysis with AA enabled
        const DWRITE_TEXTURE_TYPE type = DWRITE_TEXTURE_ALIASED_1x1;

        RECT bounds = { 0 };
        IDWriteGlyphRunAnalysis_GetAlphaTextureBounds(analysis, type, &bounds);

        glyph->xoff = (i16)bounds.left;
        glyph->yoff = (i16)bounds.top;
        glyph->w = (u16)(bounds.right - bounds.left);
        glyph->h = (u16)(bounds.bottom - bounds.top);

        u32 bitmap_size = glyph->w * glyph->h;
        glyph_bitmap = malloc(bitmap_size * sizeof(u8));
        IDWriteGlyphRunAnalysis_CreateAlphaTexture(analysis, type, &bounds, glyph_bitmap, bitmap_size);
        IDWriteGlyphRunAnalysis_Release(analysis);
    }
    return glyph_bitmap;
}

//
// glyph atlas
//

static GlyphAtlas* glyph_atlas_create()
{
    GlyphAtlas* glyph_atlas = malloc(sizeof(GlyphAtlas));
    glyph_atlas->w = GLYPH_ATLAS_WIDTH;
    glyph_atlas->h = GLYPH_ATLAS_HEIGHT;
    glyph_atlas->bitmap = malloc(glyph_atlas->w * glyph_atlas->h * sizeof(u8));
    memset(glyph_atlas->bitmap, 0, glyph_atlas->w * glyph_atlas->h * sizeof(u8));
    glyph_atlas->next_x = 0;
    glyph_atlas->next_y = 0;
    glyph_atlas->maxy = 0;
    return glyph_atlas;
}

static void glyph_atlas_destroy(GlyphAtlas* glyph_atlas)
{
    free(glyph_atlas->bitmap);
    free(glyph_atlas);
}

//
// glyph cache
//
// Note: Currently, it lacks advanced features such as LRU (Least Recently Used) management or
// other optimization techniques, serving merely as a straightforward storage mechanism for glyphs
// and their associated atlas data.
//

void glyph_cache_init(GlyphCache* glyph_cache, const u16 glyphs_length)
{
    glyph_cache->atlas = glyph_atlas_create();
    glyph_cache->glyphs = malloc(glyphs_length * sizeof(Glyph));
}

void glyph_cache_pack_codepoints(IDWriteFactory3* dwrite_factory, GlyphCache* glyph_cache, Font* font, const u32* codepoints, const u16 codepoints_length)
{
    // Insert glyph to atlas and get relative position information for glyphs & glyph_atlas
    GlyphAtlas* atlas = glyph_cache->atlas;
    for (i32 i = 0; i < codepoints_length; i++)
    {
        Glyph* glyph = &glyph_cache->glyphs[i];
        u8* glyph_bitmap = glyph_get(dwrite_factory, font, codepoints[i], glyph);

        // Update the position information of glyph and atlas
        atlas->maxy = max(atlas->maxy, atlas->next_y + glyph->h);
        Assert(atlas->maxy <= atlas->h);
        if (atlas->next_x + glyph->w > atlas->w)
        {
            Assert(atlas->maxy + glyph->h <= atlas->h);
            atlas->next_y = atlas->maxy;
            atlas->next_x = 0;
        }
        glyph->atlas_x = atlas->next_x;
        glyph->atlas_y = atlas->next_y;

        // Insert glyph bitmap into the glyph atlas bitmap
        for (u16 y = 0; y < glyph->h; y++)
        {
            for (u16 x = 0; x < glyph->w; x++)
            {
                u8 grayscale = glyph_bitmap[glyph->w * y + x];
                atlas->bitmap[(atlas->next_y + y) * atlas->w + (atlas->next_x + x)] = grayscale;
            }
        }
        atlas->next_x += glyph->w;

        // Clean
        free(glyph_bitmap);
    }
}

void glyph_cache_deinit(GlyphCache* glyph_cache)
{
    free(glyph_cache->glyphs);
    glyph_atlas_destroy(glyph_cache->atlas);
    free(glyph_cache);
}

