// TODO: Currently only include ASCII characters, custom icons from a specified font, and a small
// white region to render solid-colored rectangles.
void glyph_cache_init_and_fill(const HWND window, GlyphCache* glyph_cache, const wchar_t* font_family)
{
    glyph_cache_init(glyph_cache, GLYPHS_LENGTH);

    // Create dwrite factory & font
    IDWriteFactory3* dwrite_factory = NULL;
    DWriteCreateFactory(DWRITE_FACTORY_TYPE_SHARED, &IID_IDWriteFactory, (void**)&dwrite_factory);
    f32 dpi = (f32)GetDpiForWindow(window);

    // TODO: Need to handle DPI things
    Font* font = font_create(dwrite_factory, font_family, FONT_SIZE, dpi);

    // Pack ascii & icons glyphs
    u32 codepoints[GLYPHS_LENGTH - 1];
    {
        u32 i;
        for (i = 0; i < GLYPHS_LENGTH - 2; i++)
        {
            codepoints[i] = i + ASCII_START;
        }
        codepoints[i] = 57569;
    }
    glyph_cache_pack_codepoints(dwrite_factory, glyph_cache, font, codepoints, GLYPHS_LENGTH - 1);

    // Insert a 3x3 white region to bottom-right corner of glyph atlas
    Glyph* white_glyph = &glyph_cache->glyphs[GLYPHS_LENGTH - 1];
    white_glyph->atlas_x = GLYPH_ATLAS_WIDTH - 3;
    white_glyph->atlas_y = GLYPH_ATLAS_HEIGHT - 3;
    white_glyph->w = 3;
    white_glyph->h = 3;
    for (i32 y = 0; y < 3; y++)
    {
        for (i32 x = 0; x < 3; x++)
        {
            glyph_cache->atlas->bitmap[(white_glyph->atlas_y + y) * GLYPH_ATLAS_WIDTH + (white_glyph->atlas_x + x)] = 255;
        }
    }

    // Clean
    font_destroy(font);
    IDWriteFactory3_Release(dwrite_factory);
}
